package com.mycompany.schoolme.domain;

public class ClassDetail {

  private String id;
  private Double grade;
  
  public ClassDetail() {}

  public String getId() {
    return id;
  }

  public void setId(String id) {
    this.id = id;
  }

  public Double getGrade() {
    return grade;
  }

  public void setGrade(Double grade) {
    this.grade = grade;
  }
  
}
